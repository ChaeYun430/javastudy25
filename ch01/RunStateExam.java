package ch01;

//변수선언 및 출력

public class RunStateExam {

	public static void main(String[] args) {
		
		int x = 1 ; //정수타입의 변수 x에 1을 넣음
		int y = 2 ; //정수타입의 변수y에 2를 넣음
		int result = x + y ; //정수타입의 변수result에 변수 x+y를 넣음>>변수x와 변수y를 넣음
		
		System.out.println("x + y = " + result);
		//>>console 창에 결과를 출력한다.
	
	}//main 메서드 종료
}//RunStateExam 클래스 종료
